    private void doneBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doneBtnActionPerformed
        if (this.t.getStatus().contentEquals("new")) {
            if (new DatabaseController().markTaskAsDone(this.t)) {
                JOptionPane.showMessageDialog(null,
                    "Veikla sėkmingai pažymėta kaip atlikta!",
                    "Atlikta!",
                    JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null,
                    "Veikla nebuvo pažymėta kaip atlikta dėl klaidos!",
                    "Klaida!",
                    JOptionPane.ERROR_MESSAGE);
            }
        } else if (this.t.getStatus().contentEquals("finished")) {
            if (new DatabaseController().markTaskAsUndone(this.t)) {
                JOptionPane.showMessageDialog(null,
                    "Veikla sėkmingai pažymėta kaip neatlikta!",
                    "Neatlikta!",
                    JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null,
                    "Veikla nebuvo pažymėta kaip neatlikta dėl klaidos!",
                    "Klaida!",
                    JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_doneBtnActionPerformed

