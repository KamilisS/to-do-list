    private void setUpCategories() {
        this.categories = new DatabaseController().getUserCategories(this.s.getUserId());
        this.categoryField.removeAllItems();
        if (!this.categories.isEmpty()) {
            this.categoryField.addItem("");
        }
        for (Category o : this.categories) {
            this.categoryField.addItem(o.getName());
        }
    }
    
    private void prefillFields() {
        this.nameField.setText(this.t.getName());
        this.descField.setText(this.t.getDescription());
        this.deadlineField.setDate(this.t.getDeadline());
        this.hourField.setValue(this.t.getDeadline().getHours());
        this.minuteField.setValue(this.t.getDeadline().getMinutes());
        this.priorityBtn.setSelected(this.t.getPriority() == 1);
        if (this.t.getCategory_id() != 0) {
            int i = 0;
            for (Category o : this.categories) {
                if (o.getId() == this.t.getCategory_id()) {
                    this.categoryField.setSelectedIndex(i + 1);
                    break;
                }
                i++;
            }
        }
        if (this.t.getStatus().contentEquals("finished")) {
            this.doneBtn.setText("Žymėti kaip neatliktą");
        }
    }
