    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        this.t.setName(this.nameField.getText());
        Date d = this.deadlineField.getDate();
        System.out.println(this.deadlineField.getDate());
        d.setYear(d.getYear() + 1900);
        d.setMonth(d.getMonth() + 1);
        d.setHours(this.hourField.getValue());
        d.setMinutes(this.minuteField.getValue());
        this.t.setDeadline(d);
        System.out.println(d);
        this.t.setPriority(this.priorityBtn.isSelected() ? 1 : 0);
        this.t.setDescription(this.descField.getText());
        if (!this.categories.isEmpty() && this.categoryField.getSelectedIndex() > 0) {
            this.t.setCategory_id(this.categories.get(this.categoryField.getSelectedIndex() - 1).getId());
        } else {
            this.t.setCategory_id(0);
        }
        if (new DatabaseController().updateTask(this.t)) {
            JOptionPane.showMessageDialog(null,
                "Veikla sėkmingai paredaguota!",
                "Paredaguota!",
                JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null,
                "Veikla nebuvo paredaguota dėl klaidos! Patikrinkite formą.",
                "Klaida!",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

